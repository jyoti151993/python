
# list comprehension
my_list=list(elem for elem in 'Jyoti')
print (my_list)

# tuple comprehension
my_tuple=tuple(elem for elem in 'Jyoti')
print(my_tuple)

# set comprehension
my_set = set(elem for elem in "Expression")
print(my_set)

# dict comprehension
my_dict = dict(('key'+elem,'value'+elem) for elem in 'Energy')
print(my_dict)

